package com.czp.Technological;

public class ForTest {

	public static void main(String[] args) {
		//使用While打印0到4
		int i = 0;
		while(i<5) {
			System.out.println("while循环输出"+i);
			i++;
		}
		
		//使用for打印0到4
		for(int j = 0;j<5;j++) {
			System.out.println("for 循环输出" + j);
		}

	}

}
